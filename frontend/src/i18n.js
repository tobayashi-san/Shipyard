function detectLang() {
  const saved = localStorage.getItem('shipyard_lang');
  if (saved) return saved;
  const browser = (navigator.language || navigator.languages?.[0] || 'de').toLowerCase();
  return browser.startsWith('en') ? 'en' : 'de';
}

let currentLang = detectLang();
document.documentElement.lang = currentLang;

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('shipyard_lang', lang);
  document.documentElement.lang = lang;
}

/**
 * Translate a key, optionally substituting {varname} placeholders.
 * Falls back to the German string, then to the raw key.
 */
export function t(key, vars = {}) {
  const str = translations[currentLang]?.[key] ?? translations['de']?.[key] ?? key;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : ''));
}
